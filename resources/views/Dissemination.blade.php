            <p class="mx-auto">In the last few years, most countries have adopted participatory sensing applications as a new approach
                  to offer location-based services to mobile users. Mobile sensing includes categories such as individual,
                  participatory, crowd, social, etc. Participatory sensing applications focus on the collection of data
                  about the users and their environment using GPS-enabled mobile devices with embedded sensors, such 
                  as accelerometers, cameras, and microphones. Their wide range of application has mobilized citizens, 
                  organizations and governments, which have become increasingly attracted to utilizing any available data 
                  to solve real world problems, generate economic activities and timely response to disasters. 
                  These applications can improve the life quality of millions of potential users in areas such as
                  monitoring diets, road and traffic conditions, noise pollution, environmental and emergence response 
                  such as fire. Users of GPS-enabled devices such as smartphones, iPads and Google Glasses engage in
                  massive data collection of real time happenings and upload the same to be used by consumers. 
                  Despite technological advancements such as sensing networks using mobile phones, the deployment 
                  of participatory sensing to solve real world issues for instance urbanization challenges such as 
                  transport infrastructure, environmental management, disasters and health care remain low in developing
                  countries especially Kenya. The government, private sector and citizens living in developing countries 
                  are still experiencing implementation challenges towards full utilization of participatory sensing.
                  Data will be collected using mix paper questionnaire and-web based questionnaire, in-depth interviews
                  direct observation and literature review and document analysis. The study will employ mixed methods 
                  qualitative and quantitative.
                  The purpose of this study is to investigate and analyze the key preconditions necessary for successful
                  implementation of participatory sensing in Kenya. The findings of this research will be used to put in
                  place the necessary enabling environment for deployment of these applications in order to improve the
                  quality of life of millions of potential users. By Synchronizing and expanding available smart devices,
                  participatory sensing is expected to deliver automation, scalability, and opportunities for real-time upload,
                  processing and response. As a resource for spatial data infrastructures, some of the benefits of GPS-enabled
                  devices could also include empowering citizens by making them part of collaborative governance as well as
                  improving the information used by decision makers.


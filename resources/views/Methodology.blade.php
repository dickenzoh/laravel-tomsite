        <h4>Area of study </h4>
        <p class="mx-auto">The study will be limited to Nairobi, capital city of Kenya with regard to geographical 
            coverage. Nairobi is chosen because it is argued that Nairobi is the fastest growing city in urbanization
             in East Africa at 22 % and with highest number of internet users and, consequently, has more challenges 
             related to expanding populations putting a strain on urban infrastructure and resources (World Bank, 2016).
              With regard to sensing domain it will focus on preconditions in four domains namely road infrastructure,
               healthcare, and environment and disaster management by various public and private agencies. This domain
                where chosen because of their priority in achieving good living condition in urban centers`.

             
        </p>
        
        <h4>Research design </h4>
        <p class="mx-auto">Both quantitative and qualitative research approaches to be used in the study.
             A participatory sensing survey will be conducted to determine the necessary preconditions for
              successful implementation of participatory sensing in Kenya. Different sensing domain will
               be targeted and data collected. To gather data on participatory sensing under different task
                domains and situations, we will conduct separate study (survey) in four weeks for each in Kenya. 

             
        </p>

        <h4>Data collection, Population, sampling strategy and sample sizes. </h4>
        <p class="mx-auto">In the Kenya primary data will collected by means of questionnaire- based user study
             with 400 participants (citizens) ; in-depth interviews from 10 respondents who are CEOs of 
             SACCOs/Schools and direct observations, which will take four weeks during field visits. 
             This implies each of the four sensing domains will be done in four weeks in Kenya. Sensors
              will be developed and used to solve real-time problems in transport monitoring in Kenya.
            Additional secondary data will be collected through review of documents and literature. The other 
            respondents will include policy makers, regulators and enforcers who will be selected for interviews 
            and observation. 
            Both qualitative and quantitative methods are used. All the research materials such as questionnaires, web 
            applications, flyers, etc., will be written in English. In addition, participants will be restricted to Kenyan
            nationals.
 

             
        </p>

        <h4>Data analysis  </h4>
        <p class="mx-auto">The questionnaires will be checked to identify and eliminate any questionnaires 
            that may not be fully filled. Piloting of the questionnaire will be done on selected  transport SACCO.
             To ensure the validity of the questionnaire, we will target the Chief Executives, head of enforcement, 
             policy makers and ICT Managers of these agencies as respondents because they are the ones who possess 
             the most accurate and complete information with regard to the research problem and government operations,
              environment and ICT applications.
            The data analyses will start with qualitative data gathered through personal observations, discussions, 
            interviews and photographs being organized into relevant themes and concepts, and then interpreted by use
             of descriptive narrations. Quantitative data will be subjected to descriptive statistics such as means and
              frequencies, and to inferential statistics such as correlation analysis. Correlation indicates the strength
               and direction of a linear relationship between two or more variables. Correlation coefficients range from
                -1.0 to +1.0.
            he data analysis process also will comprise of the comparison between the responses and relevant documents
             to supplement the quality of information across the entire data set; then grouped in respect to different
              conditions and aspects.

              
        </p>

